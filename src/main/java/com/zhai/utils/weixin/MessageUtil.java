/**
 * 解析微信发来的请求（XML）
 *
 * @param message
 * @return Map<String, String>
 * @throws Exception*/
@SuppressWarnings("unchecked")
public static Map<String, String> parseXml(String message)
        throws Exception {
    // 将解析结果存储在HashMap中
    Map<String, String> map = new HashMap<String, String>();
    StringReader xmlReader = new StringReader(message);
    /**
     * **创建新的输入源SAX 解析器将使用 InputSource 对象来确定如何读取 XML 输入
     */
    InputSource xmlSource = new InputSource(xmlReader);
    /**
     * *创建一个SAXBuilder*
     */
    SAXBuilder builder = new SAXBuilder();
    /**
     * *通过输入源SAX构造一个Document**
     */
    Document document = builder.build(xmlSource);
    // 得到XML根元素
    Element root = document.getRootElement();
    // 得到根元素的所有子节点
    List<Element> elementList = root.getChildren();

    // 遍历所有子节点
    for (Element e : elementList)
        map.put(e.getName(), e.getText());
    return map;
}

    /**
     * 扩展xstream使其支持CDATA
     */
    private static XStream xstream = new XStream(new XppDriver() {
        public HierarchicalStreamWriter createWriter(Writer out) {
            return new PrettyPrintWriter(out) {
                // 对所有XML节点的转换都增加CDATA标记
                boolean cdata = true;

                @SuppressWarnings("unchecked")
                public void startNode(String name, Class clazz) {
                    super.startNode(name, clazz);
                }

                protected void writeText(QuickWriter writer, String text) {
                    if (cdata) {
                        writer.write("<![CDATA[");
                        writer.write(text);
                        writer.write("]]>");
                    } else {
                        writer.write(text);
                    }
                }
            };
        }
    });

    /**
     * 文本消息对象转换成XML
     *
     * @param textMessage 文本消息对象
     * @return xml
     */
    public static String messageToXml(TextMessage textMessage) {
        xstream.alias("xml", textMessage.getClass());
        return xstream.toXML(textMessage);
    }

